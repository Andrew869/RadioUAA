    <h1>Derechos de la Audiencia</h1>
    <p>Se reconocen como derechos de las audiencias de Radio Universidad Autónoma de Aguascalientes 94.5 FM 
        XHUAA los siguientes:</p>
    <section class="derechos">
        <ul>
            <li>El respeto a los derechos humanos, el interés superior de la niñez y la igualdad de género.</li>
            <li>Recibir contenidos libres de discriminación.</li>
            <li>Ejercer libremente, sin persecución o investigación judicial o administrativa alguna, el derecho de información, de expresión y de recepción de contenidos audiovisuales a través del servicio de radiodifusión, el que no será objeto de limitación alguna ni censura previa, y se ejercerá en los términos de la Constitución, los tratados internacionales y las leyes aplicables;</li>
            <li>Protección y defensa efectiva de los derechos de las audiencias en términos de la Constitución Política de los Estados Unidos Mexicanos, los tratados internacionales, la Ley Federal de Telecomunicaciones y Radiodifusión, los Lineamientos Generales sobre los Derechos de las Audiencias y demás disposiciones aplicables.</li>
            <li>Que la programación que se difunda, en el marco de la libertad de expresión y recepción de ideas e información, propicie:
                <ul>
                    <li>La integración de las familias</li>
                    <li> El desarrollo armónico de la niñez</li>
                    <li>El mejoramiento de los sistemas educativos</li>
                    <li>La difusión de los valores artísticos, históricos y culturales</li>
                    <li>El desarrollo sustentable</li>
                    <li>La difusión de las ideas que afirmen nuestra unidad nacional</li>
                    <li>La igualdad de género entre mujeres y hombres</li>
                    <li>La divulgación del conocimiento científico y técnico</li>
                    <li>El uso correcto del lenguaje.</li>
                </ul>
            </li>
            <li>Recibir advertencias sobre los contenidos que puedan perjudicar el libre desarrollo de la personalidad de niñas, niños y adolescentes para lo cual se atenderá al sistema de clasificación de contenidos que se establezca en las disposiciones aplicables</li>
            <li>Presentación de los títulos de los programas y su clasificación al inicio y a la mitad de éstos; para lo cual se atenderá al sistema de clasificación de contenidos que se establezca en las disposiciones aplicables;</li>
            <li>Recibir advertencias sobre determinados contenidos que puedan resultar impropios o inadecuados para los menores, de conformidad con el sistema de clasificación de contenidos de programas y películas cinematográficas que se establezca en las disposiciones reglamentarias;</li>
            <li>Recibir contenidos diarios que incluyan información sobre acontecimientos de carácter político, social, cultural, deportivo y otros asuntos de interés general, nacionales e internacionales;</li>
            <li>Recibir contenidos que reflejen la pluralidad ideológica, política, social y cultural y lingüística de la Nación</li>
            <li>Recibir información con veracidad y oportunidad</li>
            <li>Que se diferencie con claridad la información noticiosa de la opinión de quien la presenta</li>
            <li>Que se aporten elementos para distinguir entre la publicidad y el contenido de un programa</li>
            <li>Recibir programación que incluya diferentes géneros que respondan a la expresión de la diversidad y pluralidad de ideas y opiniones que fortalezcan la vida democrática de la sociedad</li>
            <li>Equilibrio entre la publicidad y el conjunto de la programación diaria</li>
            <li>No transmisión de publicidad o propaganda presentada como información periodística o noticiosa</li>
            <li>Que la publicidad cumpla con los requisitos de clasificación, incluidas las franjas horarias, que contemplen las disposiciones aplicables</li>
            <li>Que la publicidad no presente conductas o situaciones en las que la falta de un producto o servicio sea motivo de discriminación de cualquier índole</li>
            <li>Que se respeten los horarios de los programas y que se avise con oportunidad los cambios a los mismos y se incluyan avisos parentales</li>
            <li>Ejercer el derecho de réplica, en términos de la ley reglamentaria</li>
            <li>Que se mantenga la misma calidad y niveles de audio durante la programación, incluida la publicidad</li>
            <li>Existencia y cumplimiento de un Código de Ética</li>
            <li>Existencia y difusión adecuada y oportuna de la rectificación, recomendación o propuesta de acción que corresponda al caso, según el derecho que como audiencia haya sido violado, y</li>
            <li>Contar con mecanismos de programas que fomenten y contribuyan a la alfabetización mediática, entendida ésta como las acciones que tienen la finalidad de promover la capacidad de análisis, comprensión y evaluación que permitan a las audiencias, ejercer los derechos inherentes a tal carácter para la eficiente utilización de los contenidos proporcionados a través del servicio de radiodifusión</li>
        </ul>
    </section>
